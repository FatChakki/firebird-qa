import pytest
from firebird.qa import *

db = db_factory()

test_script = """
    create table t1 (pk1 integer, col varchar(10));
    create table t2 (pk2 integer, fk1 varchar(10), col varchar(10),
    foreign key (fk1) references t1 (pk1));
"""

# QA_GLOBALS -- dict, is defined in qa/plugin.py, obtain settings
# from act.files_dir/'test_config.ini':
#
addi_subst_settings = QA_GLOBALS['schema_n_quotes_suppress']
addi_subst_tokens = addi_subst_settings['addi_subst']

substitutions = [('[ \t]+', ' ')]
for p in addi_subst_tokens.split(' '):
    substitutions.append( (p, '') )

act = isql_act('db', test_script, substitutions = substitutions)

expected_stdout = """
    Statement failed, SQLSTATE = 42000
    unsuccessful metadata update
    -CREATE TABLE T2 failed
    -could not find UNIQUE or PRIMARY KEY constraint in table T1 with specified columns
"""

@pytest.mark.version('>=3.0')
def test_1(act: Action):
    act.expected_stdout = expected_stdout
    act.execute(combine_output = True)
    assert act.clean_stdout == act.clean_expected_stdout

